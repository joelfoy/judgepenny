<p><img src="images/judge.png"/ style="float:left; margin:0px 6px 6px 0px;"><span style="color:#4025D9;"><i>The Honorable Penny Brown Reynolds Biography</i></span></p><p>Judge Penny Brown Reynolds has distinguished herself as a voice of wisdom, reason, and change.  She is an Emmy-nominated multi-gifted national television personality and commentator on issues of law, politics, religion, as well as women and children's issues.   </p>
<br />
<p>Judge Brown Reynolds served as a state trial court judge in Atlanta, Georgia for nearly a decade.  Her extensive legal career includes a historic position as Executive Counsel to Georgia's Governor, where she was the first African American to hold such a position.  Ebony and Jet magazines recognized her for this historic accomplishment. She is a former prosecutor and Assistant Attorney General.</p>
<br />
<p>Judge Brown Reynolds presides over the internationally syndicated television show, "Family Court with Judge Penny." She is an eloquent speaker and television commentator on the most relevant issues of our day.  She lectures across this country at numerous colleges and universities, conferences and conventions.  She assists corporations with diversity issues and crisis management.  Additionally, she has brought the same passion and commitment she delivers to the law to serving as an ordained minister, rendering messages of hope while confronting the challenging issues of our day.</p>
<br />
<p>Judge Brown Reynolds is the author of several published articles. Her book, 7 Steps to Peace of Mind is compelling and moving.  Additionally, she is the author and co-editor of, Women and the Law: A Guide to Women's Legal Rights in Georgia. </p>
<br />
<p>Judge Brown Reynolds earned a Bachelor of Science, Cum Laude, from Georgia State University after three years of study, a Juris Doctor from Georgia State University College of Law and a Master's degree from the Interdenominational Theological Center, graduating first in her class with highest honors.  Judge Brown Reynolds is the recipient of numerous awards and is a member of numerous bar associations.</p>
<br />
<p>She has founded a 501(c) (3) non-profit corporation, The Judge Penny Brown Reynolds Foundation Inc., for the purpose of restoring families and empowering women and young people.  She is a veteran social activist and is President and CEO of Divine Destiny Productions, LLC.</p>
<br />
<p>Judge Brown Reynolds and her husband, Rev. Edward S. Reynolds are the proud parents of two grown sons, Michael and Fela and a granddaughter, Pelly-Marie.</p>

