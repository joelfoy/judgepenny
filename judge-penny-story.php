<h2>Experienced.</h2>
<p><img src="images/story.jpg"/ style="float:left; margin:0px 6px 6px 0px;"><em style=" color:#4025D9;">Excerpt from the Introduction to Judge Penny's Book, 7 Steps to Peace of Mind.</em></p>


<p>I want to tell you a personal story so that you can understand how situations outside of ourselves can help shape how we view ourselves and what we can expect from life. I was born to a single mother. I grew up not knowing my father. At some point, I was told about my father, but the man never showed love or interest in me. I believe it was because he felt that I was not his child. </p>

<p>Growing up in a majority Catholic small town in Laplace, Louisiana was not easy for someone who lived with her single mother and grandparents in the same house.
While attending a brand new Catholic school, all of the girls in my third grade class were invited to a classmate's birthday tea party. In my opinion, she was a little girl who had everything. She lived in a big brick house and she had parents who were both educators. She was so very pretty. </p>

<p>Even as a child, I was never envious of her or anyone else. I just wondered what it would be like to have the life she had. I did believe, however, that somehow she was better than me and my family because of her house and parents. Needless to say, I was so happy to be able to attend this tea party. </p>

<p>My mother took me to New Orleans on the bus and we shopped on Canal Street. She bought me a new pink dress with a jacket and pleats on the bottom. I had a white straw hat, white patent leather shoes and purse and white gloves. I remember it as if it was yesterday because I have always liked fashion. We bought the birthday girl a wonderful present and my mother wrapped it nicely. I barely slept for days because I was so excited. The day of the party finally arrived. I got all dressed up and I can remember my grandmother and my mother standing on the front porch of our old shotgun house, in a place we called, "down the road," waving as my grandfather and I pulled off and headed over the railroad tracks to the party.</p>

<p>It was a Sunday afternoon and my grandfather dropped me off in his pickup truck at the front door. I jumped out of the truck because I saw a few of my other friends headed to the front door at just about the same time. My grandfather told me to call him when I was ready to be picked up because we lived in the neighboring town. All of my classmates were at the door. Everyone was dressed so pretty, but I felt that I looked the best because my mother had such great taste. I finally felt like I was one of them. I finally felt like I belonged. It was the happiest day of my life.</p>

<p>The girl's mother answered the door and allowed all of us into their beautiful foyer. Everyone saw the birthday gir land ran toward her. But her mother grabbed me by the arm, pulled me aside and said in the harshest tone I had everheard, "What are you doing here?" My insides began to tremble. I instantly knew in my spirit that it was too good to be true, but I was sure I could explain. I opened my white purse and pulled out the invitation and showed it to her. I remember looking up at her with as mile on my face, while she glared at me with contempt.</p>

<p>She said the invitation was a mistake because she was not going to have a bastard child at her daughter's party. She then opened the door and closed it, leaving me and her daughter's present on the outside. I was shocked. I began walking away when I remembered the present. I turned around and leaned it up against the front door. I could hear laughter on the other side of the door. This was the first door that had been closed on my life. I turned around and started to walk. All I can remember is walking. We lived a long distance, but I kept walking and walking and walking. I somehow knew I would never be the same. I cried all the way home. I made a decision during that long walk that would impact my life and how I viewed things for years to come.</p>

<p>I arrived home hours later and opened the screen door of the house. While growing up, I never remembered the front door being locked during the daytime. I opened the door, walked past the living room, through the bedroom and into the kitchen. Granny was at the kitchen table. She said that she thought I was supposed to call. I lied and said that I had been dropped off by one of my friend's parents. My mother walked into the kitchen from the back porch. </p>

<p>I told them all the wonderful details about a party I was not good enough to attend. Granny and my mother both died never knowing what really happened on the day that changed my life forever. You must understand one important thing about the story. Had I told my mother what that awful woman had done to me, she would have given that woman a piece of her mind. </p>

<p>I desperately wanted peace, not only for me but for everyone else around me. I lied that day to protect my mother and make it all go away. I did not want to believe that I was not good enough. I did not want to accept what the woman had said to me. Therefore, I pretended that all was well. Somehow it made things better at the time, but how could I know that it would haunt me for most of my life. </p>

<p>That Sunday afternoon changed my life forever. My peace of mind was taken away from me at the age of seven or eight years old. I spent most of my adult life feeling as if I never belonged at the party or anywhere else. While growing up,I used my education and extracurricular activities to prove that what I was feeling was wrong. The message stayed in my spirit for many years. I frequently made decisions about my life with the motivation to prove to the world that I belonged and I was good enough. I wanted to prove that I was good enough to attend that party and any other party I wanted to attend. If we all took a moment to reflect, I am certain that you can pinpoint the time when your peace was stolen from you.</p>

<p>Many years later, I actually thank God for giving me the mindset that allowed me to fight to prove the world wrong. The experience was one in which no little girl should have had to go through, but it gave me a drive in life that I may not have had but for that experience.</p>
<p>I later learned that my friend's life was not quite the storybook that I had imagined. Sure, the family lived in a big house and the parents were married and educated, but the little girl's mother was an alcoholic. Learning the truth about her life, however, did not help me find peace of mind.</p>

<p>I had to learn that it wasn't where I came from or what others thought of me that mattered.  What is important is what I've managed to do with my life. Let my story serve as an example that it doesn't matter how you start off in life, but rather what you do with it.  The legacy I wish to leave is a legacy of empowerment and purpose.  Don't allow anyone to tell you that you don't matter because you are different or because you may not be like everyone else. </p>

